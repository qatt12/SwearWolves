class velocity():
    def __init__(self, x=0, y=0, **kwargs):
        self.x = x
        self.y = y
        mag = 1
        if 'mag' in kwargs:
            mag = kwargs['mag']
        elif "var_mag" in kwargs:
            mag = (kwargs['var_mag'][0] - kwargs['var_mag'][1]) + random.randint(0, int(2*kwargs['var_mag'][1]))
        if 'dir' in kwargs:
            self.x = mag * kwargs['dir'][0]
            self.y = mag * kwargs['dir'][1]
        if "angle" in kwargs:
            pass

        self.p_x = x % 1
        self.t_x = 0.0
        self.p_y = y % 1
        self.t_y = 0.0

    def __call__(self, x=False, y=False, *args, **kwargs):
        if x:
            self.x = x
            self.p_x = x % 1
        if y:
            self.y = y
            self.p_y = y % 1

        if 'add_x' in kwargs:
            self.x += kwargs['add_x']
        if 'add_y' in kwargs:
            self.y += kwargs['add_y']

        if 'recalc_x' in kwargs:
            self.x += kwargs['recalc_x']
            self.p_x = self.x%1
        if 'recalc_y' in kwargs:
            self.y += kwargs['recalc_y']
            self.p_y = self.y%1

        r_x = self.x
        r_y = self.y
        self.t_x += self.p_x
        self.t_y += self.p_y
        if self.t_x > 1:
            self.t_x -= 1
            r_x += 1
        elif self.t_x < -1:
            self.t_x += 1
            r_x -= 1
        if self.t_y > 1:
            self.t_y -= 1
            r_y += 1
        elif self.t_y < -1:
            self.t_y += 1
            r_y += 1
        return r_x, r_y

    def __getitem__(self, item):
        if item == '0' or item == 0 or item == 'x':
            return self.x
        elif item == '1' or item == 1 or item == 'y':
            return self.y


